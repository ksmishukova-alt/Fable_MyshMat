// ─────────────────────────────────────────────
// «Головы и ноги» — параметрический генератор
// метод: (1) участники и ноги → (2) все «минимальные» → (3) разница → (4) делим
// ─────────────────────────────────────────────
interface HeadsLegsParams {
  a: string; // животное с бОльшим числом ног
  aLegs: number;
  b: string;
  bLegs: number;
  heads: number;
  countA: number; // ответ: сколько животных a
  plural: [string, string]; // родительный падеж мн.ч: ["зайцев","змей"]
}

function headsLegs(p: HeadsLegsParams, id: string, level: 1 | 2 | 3, ord: number): OlympiadProblem {
  const legs = p.countA * p.aLegs + (p.heads - p.countA) * p.bLegs;
  const minLegs = p.heads * p.bLegs;
  const diff = legs - minLegs;
  const per = p.aLegs - p.bLegs;
  const statement = `В загоне сидят ${p.plural[0]} и ${p.plural[1]}. Всего у них ${p.heads} голов и ${legs} ног. Сколько ${p.plural[0]} в загоне?`;
  const answer = String(p.countA);

  const guidedSteps: GuidedStep[] = [
    {
      id: `${id}-s1`,
      prompt: `Сколько ног у каждого? Выбери пару.`,
      options: [
        { id: "o1", label: `${p.a} — ${p.aLegs}, ${p.b} — ${p.bLegs}`, isCorrect: true },
        { id: "o2", label: `${p.a} — ${p.bLegs}, ${p.b} — ${p.aLegs}`, isCorrect: false },
        { id: "o3", label: `у всех по ${p.aLegs}`, isCorrect: false },
      ],
      explain: `Точно! У каждого зверя ${p.heads} голов не бывает, а вот ноги считаем по видам.`,
      hint: `Вспомни, как выглядит ${p.b}: сколько у него ног?`,
    },
    {
      id: `${id}-s2`,
      prompt: `Представь, что ВСЕ ${p.heads} голов — это «${p.b}». Сколько было бы ног?`,
      accepted: [String(minLegs)],
      explain: `${p.heads} × ${p.bLegs} = ${minLegs}. Это «минимальный» вариант.`,
      hint: `Умножь число голов на ${p.bLegs}.`,
    },
    {
      id: `${id}-s3`,
      prompt: `На сколько настоящих ног больше, чем в «минимальном» варианте (${minLegs})?`,
      accepted: [String(diff)],
      explain: `${legs} − ${minLegs} = ${diff}. Эти «лишние» ноги дали ${p.plural[0]}.`,
      hint: `Вычти ${minLegs} из ${legs}.`,
    },
    {
      id: `${id}-s4`,
      prompt: `Каждая замена «${p.b}» на «${p.a}» добавляет ${per} ноги. Сколько же ${p.plural[0]}?`,
      accepted: [answer],
      explain: `${diff} ÷ ${per} = ${answer}. Ответ: ${answer} ${p.plural[0]}!`,
      hint: `Раздели ${diff} на ${per}.`,
    },
  ];

  const base: OlympiadProblem = {
    id,
    topicId: "heads-legs",
    level,
    order: ord,
    title: `${p.a} и ${p.b}`,
    statement,
    actionsCount: 3,
    expectedAnswer: answer,
    acceptedAnswers: [answer, `${answer} ${p.plural[0]}`],
    hints: [
      `Попробуй представить, что все звери — «${p.b}». Сколько тогда ног?`,
      `Настоящих ног ${legs}, «минимальных» — ${minLegs}. Куда делась разница?`,
      `Разница ${diff} ног. Одна замена «${p.b}»→«${p.a}» даёт +${per} ноги. Раздели!`,
    ],
    algebra: {
      formula: `x·${p.aLegs} + (${p.heads}−x)·${p.bLegs} = ${legs}`,
      explanation: `Обозначим число ${p.plural[0]} за x. Тогда ног: x·${p.aLegs} + (${p.heads}−x)·${p.bLegs} = ${legs}. Решая: x = ${answer}.`,
    },
    rewardStars: level * 10,
  };

  if (level === 1) {
    return { ...base, guidedSteps };
  }
  if (level === 2) {
    return {
      ...base,
      support: {
        planCards: [
          "Понять, сколько ног у каждого зверя",
          "Посчитать ноги, если бы все были «минимальными»",
          "Найти разницу с настоящим числом ног",
          "Разделить разницу на «цену» одной замены",
        ],
        planOrder: [0, 1, 2, 3],
        actions: [
          {
            kinds: ["Умножение голов на меньшее число ног", "Сложение всех ног", "Вычитание голов"],
            correctKind: 0,
            acceptedExpressions: [`${p.heads}*${p.bLegs}`, `${p.heads}×${p.bLegs}`, `${p.heads}·${p.bLegs}`],
            value: minLegs,
          },
          {
            kinds: ["Вычитание «минимальных» ног из настоящих", "Деление ног на головы", "Сложение голов и ног"],
            correctKind: 0,
            acceptedExpressions: [`${legs}-${minLegs}`, `${legs}−${minLegs}`],
            value: diff,
          },
          {
            kinds: ["Деление разницы на цену замены", "Умножение разницы на два", "Вычитание из числа голов"],
            correctKind: 0,
            acceptedExpressions: [`${diff}/${per}`, `${diff}:${per}`, `${diff}÷${per}`],
            value: p.countA,
          },
        ],
      },
    };
  }
  return base; // L3 — листочек
}

const HL: Omit<HeadsLegsParams, "countA">[] = [
  { a: "Заяц", aLegs: 4, b: "Змея", bLegs: 0, heads: 10, plural: ["зайцев", "змей"] },
  { a: "Курица", aLegs: 2, b: "Змея", bLegs: 0, heads: 12, plural: ["куриц", "змей"] },
  { a: "Корова", aLegs: 4, b: "Курица", bLegs: 2, heads: 9, plural: ["коров", "куриц"] },
  { a: "Паук", aLegs: 8, b: "Жук", bLegs: 6, heads: 8, plural: ["пауков", "жуков"] },
  { a: "Краб", aLegs: 10, b: "Осьминог", bLegs: 8, heads: 7, plural: ["крабов", "осьминогов"] },
];

function headsLegsSet(level: 1 | 2 | 3): OlympiadProblem[] {
  // на каждом уровне те же 5 «сюжетов», но разные числа
  const counts = level === 1 ? [3, 5, 4, 3, 2] : level === 2 ? [6, 7, 5, 5, 4] : [7, 9, 6, 6, 5];
  return HL.map((h, i) =>
    headsLegs(
      { ...h, countA: Math.min(counts[i], h.heads - 1) },
      `hl-${level}-${i + 1}`,
      level,
      i + 1,
    ),
  );
}

// ─────────────────────────────────────────────
// Чётность / Логика / Дирихле — рукописные
// ─────────────────────────────────────────────
function simple(
  topicId: string,
  idp: string,
  level: 1 | 2 | 3,
  ord: number,
  title: string,
  statement: string,
  answer: string,
  accepted: string[],
  hints: string[],
  guidedSteps?: GuidedStep[],
  support?: OlympiadProblem["support"],
): OlympiadProblem {
  return {
    id: `${idp}-${level}-${ord}`,
    topicId,
    level,
    order: ord,
    title,
    statement,
    expectedAnswer: answer,
    acceptedAnswers: [answer, ...accepted],
    hints,
    guidedSteps: level === 1 ? guidedSteps : undefined,
    support: level === 2 ? support : undefined,
    rewardStars: level * 10,
  };
}

