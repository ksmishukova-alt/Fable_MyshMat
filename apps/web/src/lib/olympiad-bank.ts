export const PROBLEMS: OlympiadProblem[] = [
  ...headsLegsSet(1),
  ...headsLegsSet(2),
  ...headsLegsSet(3),
  ...PARITY,
  ...LOGIC,
  ...DIRICHLET,
];

export function problemsFor(topicId: string, level: 1 | 2 | 3): OlympiadProblem[] {
  return PROBLEMS.filter((p) => p.topicId === topicId && p.level === level).sort(
    (a, b) => a.order - b.order,
  );
}

export function problemById(id: string): OlympiadProblem | undefined {
  return PROBLEMS.find((p) => p.id === id);
}
