function fanL2(parts: number, ord: number): OlympiadProblem {
  const total = fanCount(parts);
  return {
    ...base(
      `cf-2-${ord}`,
      2,
      ord,
      `«Веер» из ${parts} частей`,
      `Основание треугольника разбито на ${parts} части линиями из вершины. Сколько всего треугольников? Найди ошибку в чужом решении и посчитай верно.`,
      { kind: "fan", parts },
      total,
      [
        "Каждый треугольник — несколько СОСЕДНИХ частей.",
        `Считай по размерам: ${parts} одиночных, ${parts - 1} двойных…`,
        `Формула веера: ${parts}·${parts + 1}÷2.`,
      ],
    ),
    support: {
      planCards: [
        "Заметить: треугольник = соседние части веера",
        "Посчитать треугольники каждого размера",
        "Сложить или применить формулу p·(p+1)÷2",
      ],
      planOrder: [0, 1, 2],
      findError: {
        lines: [
          `Одиночных треугольников ${parts}.`,
          `Из двух соседних частей — ${parts - 1}, из трёх — ${parts - 2} и так далее.`,
          `Большой треугольник считать не нужно — это рамка, а не фигура.`,
          `Итого ${total - 1}.`,
        ],
        wrongLine: 2,
        acceptedFixes: [
          "большой тоже треугольник",
          "большой треугольник считается",
          "нужно считать",
          `итого ${total}`,
        ],
      },
    },
  };
}

