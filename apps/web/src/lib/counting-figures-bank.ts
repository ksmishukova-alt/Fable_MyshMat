/**
 * Тема «Подсчёт фигур» (олимпиадный формат) + добор задач по другим темам.
 *
 * Лайфхаки из методики (СУНЦ МГУ, разборы олимпиадных кружков):
 *  1. Считать по размерам: сначала одиночные фигуры, потом составные из 2, из 3…
 *  2. Систематический перебор с маркировкой (буквы у точек/частей) — ничего не пропустить
 *     и не посчитать дважды.
 *  3. Формулы-ускорители: отрезки по n точкам = n(n−1)/2; «веер» из вершины с основанием
 *     из p частей = p(p+1)/2 треугольников; квадраты в сетке n×n = 1²+2²+…+n²;
 *     прямоугольники в сетке m×n = (сумма по вертикали) × (сумма по горизонтали).
 *  4. Проверка: маленьких фигур всегда больше всего; итог не меньше их числа.
 */
import type { OlympiadProblem, GuidedStep, FigureSpec } from "@/types/olympiad";

// ── формулы (используются и в генераторах, и в автотесте) ──
export const segCount = (points: number) => (points * (points - 1)) / 2;
export const fanCount = (parts: number) => (parts * (parts + 1)) / 2;
export const gridSquares = (n: number) => {
  let s = 0;
  for (let k = 1; k <= n; k++) s += k * k;
  return s;
};
export const rectCount = (rows: number, cols: number) =>
  (((rows + 1) * rows) / 2) * (((cols + 1) * cols) / 2);

const LETTERS = "ABCDEFGHKLMN";

function base(
  id: string,
  level: 1 | 2 | 3,
  ord: number,
  title: string,
  statement: string,
  figure: FigureSpec,
  answer: number,
  hints: string[],
): OlympiadProblem {
  return {
    id,
    topicId: "counting-figures",
    level,
    order: ord,
    title,
    statement,
    figure,
    expectedAnswer: String(answer),
    acceptedAnswers: [String(answer)],
    hints,
    rewardStars: level * 10,
  };
}

// ─────────────────────────────────────────────
// L1 — тренировка: считаем по размерам, шаг за шагом
// ─────────────────────────────────────────────
function segmentsL1(points: number, ord: number): OlympiadProblem {
  const total = segCount(points);
  const steps: GuidedStep[] = [];
  for (let len = 1; len < points; len++) {
    const cnt = points - len;
    steps.push({
      id: `cf-seg-${points}-s${len}`,
      prompt:
        len === 1
          ? `Лайфхак «по размерам»: сначала самые маленькие. Сколько отрезков между СОСЕДНИМИ точками?`
          : `Теперь длиннее: сколько отрезков из ${len} маленьких подряд (например, ${LETTERS[0]}${LETTERS[len]})?`,
      accepted: [String(cnt)],
      explain:
        len === 1
          ? `Верно, ${cnt}! Маленькие посчитаны — идём к составным.`
          : `Да, ${cnt}. Двигаем «окно» слева направо — так не пропустим ни одного.`,
      hint: `Веди пальцем: первый начинается в ${LETTERS[0]}, последний — за ${len} шагов до конца.`,
    });
  }
  steps.push({
    id: `cf-seg-${points}-sum`,
    prompt: `Складываем все размеры: ${Array.from({ length: points - 1 }, (_, i) => points - 1 - i).join(" + ")} = ?`,
    accepted: [String(total)],
    explain: `Итого ${total}! Запомни формулу-ускоритель: точек ${points} → ${points}·${points - 1}÷2 = ${total}.`,
    hint: `Просто сложи числа из предыдущих шагов.`,
  });
  return {
    ...base(
      `cf-1-${ord}`,
      1,
      ord,
      `Отрезки: ${points} точки на прямой`,
      `На прямой отметили ${points} точки: ${LETTERS.slice(0, points).split("").join(", ")}. Сколько всего отрезков с концами в этих точках?`,
      { kind: "segments", points },
      total,
      [
        "Лайфхак: считай по размерам — сначала маленькие, потом длиннее.",
        `Маленьких отрезков ${points - 1}. Отрезков из двух маленьких — ${points - 2}. Видишь закономерность?`,
        `Сложи ${Array.from({ length: points - 1 }, (_, i) => points - 1 - i).join("+")}.`,
      ],
    ),
    guidedSteps: steps,
  };
}

function fanL1(parts: number, ord: number): OlympiadProblem {
  const total = fanCount(parts);
  const steps: GuidedStep[] = [];
  for (let len = 1; len <= parts; len++) {
    const cnt = parts - len + 1;
    steps.push({
      id: `cf-fan-${parts}-s${len}`,
      prompt:
        len === 1
          ? `Сколько САМЫХ МАЛЕНЬКИХ треугольников (из одной части)?`
          : `Сколько треугольников из ${len} соседних частей?`,
      accepted: [String(cnt)],
      explain: len === parts ? `Да — большой треугольник один!` : `Верно, ${cnt}.`,
      hint: `Считай слева направо, «окно» из ${len} частей.`,
    });
  }
  steps.push({
    id: `cf-fan-${parts}-sum`,
    prompt: `Сложи всё: ${Array.from({ length: parts }, (_, i) => parts - i).join(" + ")} = ?`,
    accepted: [String(total)],
    explain: `${total} треугольников! Формула веера: ${parts}·${parts + 1}÷2 = ${total}.`,
  });
  return {
    ...base(
      `cf-1-${ord}`,
      1,
      ord,
      `Треугольник-«веер» (${parts} части)`,
      `Из верхней вершины треугольника провели линии к основанию — получилось ${parts} маленьких части. Сколько ВСЕГО треугольников на рисунке?`,
      { kind: "fan", parts },
      total,
      [
        "Каждый треугольник здесь — несколько СОСЕДНИХ частей. Считай по размерам!",
        `Одиночных — ${parts}, из двух соседних — ${parts - 1}…`,
        `Сложи ${Array.from({ length: parts }, (_, i) => parts - i).join("+")}.`,
      ],
    ),
    guidedSteps: steps,
  };
}

function gridL1(ord: number): OlympiadProblem {
  // 2×2: 4 маленьких + 1 большой = 5
  return {
    ...base(
      `cf-1-${ord}`,
      1,
      ord,
      "Квадраты в сетке 2×2",
      "Перед тобой сетка 2×2. Сколько всего квадратов на рисунке? (Не забудь про большой!)",
      { kind: "grid", size: 2 },
      5,
      [
        "Считай по размерам: сначала клетки 1×1, потом квадраты побольше.",
        "Маленьких квадратов 4. А есть ли квадрат 2×2?",
        "4 маленьких + 1 большой.",
      ],
    ),
    guidedSteps: [
      {
        id: "cf-grid2-s1",
        prompt: "Сколько маленьких квадратов 1×1?",
        accepted: ["4"],
        explain: "Да, 4 клетки.",
        hint: "Посчитай клетки сетки.",
      },
      {
        id: "cf-grid2-s2",
        prompt: "Хитрый вопрос: есть ли квадрат побольше? Сколько квадратов 2×2?",
        accepted: ["1"],
        explain: "Точно — вся сетка целиком тоже квадрат!",
        hint: "Посмотри на рисунок целиком.",
      },
      {
        id: "cf-grid2-s3",
        prompt: "Итого квадратов?",
        accepted: ["5"],
        explain: "5! Лайфхак: большую фигуру забывают чаще всего.",
      },
    ],
  };
}

// ─────────────────────────────────────────────
// L2 — с поддержкой: план + действия-формулы
// ─────────────────────────────────────────────
function segmentsL2(points: number, ord: number): OlympiadProblem {
  const total = segCount(points);
  return {
    ...base(
      `cf-2-${ord}`,
      2,
      ord,
      `Отрезки: ${points} точек`,
      `На прямой ${points} точек. Сколько всего отрезков с концами в них? Собери план и посчитай формулой.`,
      { kind: "segments", points },
      total,
      [
        "Каждый отрезок — это пара точек. Сколько пар?",
        `Каждая из ${points} точек дружит с ${points - 1} другими — но каждую пару так посчитаем дважды!`,
        `${points}·${points - 1}÷2.`,
      ],
    ),
    actionsCount: 2,
    support: {
      planCards: [
        "Понять: отрезок = пара точек",
        "Умножить число точек на число «партнёров» каждой",
        "Разделить пополам — каждая пара посчитана дважды",
      ],
      planOrder: [0, 1, 2],
      actions: [
        {
          kinds: [
            "Умножить точки на (точки − 1)",
            "Сложить все точки",
            "Умножить точки сами на себя",
          ],
          correctKind: 0,
          acceptedExpressions: [`${points}*${points - 1}`, `${points}×${points - 1}`, `${points}·${points - 1}`],
          value: points * (points - 1),
        },
        {
          kinds: ["Разделить на 2 (пары посчитаны дважды)", "Вычесть 2", "Умножить на 2"],
          correctKind: 0,
          acceptedExpressions: [`${points * (points - 1)}/2`, `${points * (points - 1)}:2`, `${points * (points - 1)}÷2`],
          value: total,
        },
      ],
    },
  };
}

